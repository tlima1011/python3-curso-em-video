def somar(n1=0, n2=0):
    return f'A soma do número {n1} e {n2} é = {n1 + n2}'


def subtrair(n1=0, n2=0):
    return f'A subtração do número {n1} e {n2} é = {n1 - n2}'


def mutliplicar(n1=0, n2=0):
    return f'A mulitiplicação dos número {n1} e {n2} é = {n1 * n2}'


def dividir(n1=0, n2=0):
    return f'A divisão dos número {n1} e {n2} é = {n1 / n2}'